SELECT DISTINCT P.PROJNO, P.PROJNAME, EA.ACTNO
FROM PROJECT P INNER JOIN EMP_ACT EA 
	 ON P.PROJNO = EA.PROJNO
WHERE P.PROJNO LIKE 'AD%' OR P.PROJNO LIKE '%II'
ORDER BY P.PROJNO, EA.ACTNO;

6
SELECT DISTINCT P.PROJNO, P.PROJNAME, EA.EMPNO, E.LASTNAME, EA.ACTNO
FROM PROJECT P INNER JOIN EMP_ACT EA 
	 ON P.PROJNO = EA.PROJNO
			   INNER JOIN EMPLOYEE E ON EA.EMPNO = E.EMPNO
WHERE P.PROJNO IN ('AD3113', 'IF2000', 'MA2112', 'OP2012')
ORDER BY P.PROJNO, EA.ACTNO;

7
SELECT E.EMPNO, P.PROJNO, P.PROJNAME, EA.ACTNO, P.PRSTDATE
FROM EMP_ACT EA	INNER JOIN EMPLOYEE E 
	 ON EA.EMPNO = E.EMPNO
				INNER JOIN PROJECT P 
	 ON EA.PROJNO = P.PROJNO
WHERE EA.EMSTDATE BETWEEN '2011-10-01' AND '2011-10-31'
ORDER BY P.PROJNO, E.EMPNO, EA.ACTNO;

8
SELECT E.WORKDEPT, E.LASTNAME, P.PROJNAME, EA.ACTNO
FROM EMPLOYEE E INNER JOIN EMP_ACT EA 
	 ON E.EMPNO = EA.EMPNO
				INNER JOIN PROJECT P 
	 ON EA.PROJNO = P.PROJNO
WHERE E.WORKDEPT = 'A00'
ORDER BY E.EMPNO, EA.ACTNO;

9
SELECT DISTINCT E.WORKDEPT, E.LASTNAME, P.PROJNAME, EA.ACTNO
FROM EMPLOYEE E INNER JOIN EMP_ACT EA 
	 ON E.EMPNO = EA.EMPNO
				INNER JOIN PROJECT P 
	 ON EA.PROJNO = P.PROJNO
WHERE E.WORKDEPT BETWEEN 'A00' AND 'C01'
ORDER BY E.WORKDEPT, E.LASTNAME, EA.ACTNO;

10
SELECT EA.ACTNO, D.MGRNO, EA.EMSTDATE, P.PROJNO, E.LASTNAME
FROM EMP_ACT EA INNER JOIN PROJECT P 
	 ON EA.PROJNO = P.PROJNO
				INNER JOIN DEPARTMENT D 
	 ON P.DEPTNO = D.DEPTNO
				INNER JOIN EMPLOYEE E 
	 ON EA.EMPNO = E.EMPNO
WHERE EA.EMSTDATE >= '2011-10-15'
ORDER BY EA.ACTNO, EA.EMSTDATE;

11
SELECT E.WORKDEPT, M.LASTNAME, E.LASTNAME, E.HIREDATE, M.HIREDATE
FROM EMPLOYEE E INNER JOIN EMPLOYEE M 
	 ON E.WORKDEPT = M.WORKDEPT AND M.JOB = 'MANAGER'
WHERE E.HIREDATE < M.HIREDATE
	 AND E.WORKDEPT IN ('A00', 'C01', 'D01', 'E01')
ORDER BY E.LASTNAME;

