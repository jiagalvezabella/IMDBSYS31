SELECT E.LASTNAME, E.FIRSTNAME, D.DEPTNAME
FROM EMPLOYEE E INNER JOIN DEPARTMENT D 
	 ON E.WORKDEPT = D.DEPTNO
ORDER BY E.FIRSTNAME, E.LASTNAME, D.DEPTNAME;

2
SELECT E.LASTNAME, E.FIRSTNAME, E.JOB, D.DEPTNAME
FROM EMPLOYEE E INNER JOIN DEPARTMENT D 
	 ON E.WORKDEPT = D.DEPTNO
WHERE D.DEPTNO BETWEEN 'AD2' AND 'D22' AND E.JOB != 'MANAGER'
ORDER BY E.FIRSTNAME, E.LASTNAME, E.JOB, D.DEPTNAME;

3
SELECT D.DEPTNAME, E.LASTNAME, E.FIRSTNAME
FROM DEPARTMENT D INNER JOIN EMPLOYEE E 
	 ON D.MGRNO = E.EMPNO
ORDER BY D.DEPTNAME;

4
SELECT D.DEPTNAME, E.LASTNAME, E.FIRSTNAME
FROM DEPARTMENT D INNER JOIN EMPLOYEE E 
	 ON D.DEPTNO = E.WORKDEPT
WHERE E.JOB = 'MANAGER'
ORDER BY D.DEPTNAME;

