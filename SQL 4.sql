SELECT F.FID, F.FNAME, FL.SBJCODE, FL.SBJTITLE, FL.UNITS, FL.SBJCODE 
FROM FACULTY F RIGHT OUTER JOIN FACULTY_LOAD FL 
	ON F.FID = FL.FLOAD 
ORDER BY FL.SBJCODE;

