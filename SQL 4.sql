SELECT F.FID, F.FNAME, F.GENDER, F.RANK, FL.SBJCODE, FL.SBJTITLE, C.COLCODE, C.COLNAME
FROM FACULTY F  FULL OUTER JOIN FACULTY_LOAD FL ON FL.FLOAD = F.FID
				FULL OUTER JOIN COLLEGE C ON C.COLCODE = F.COLCODE
ORDER BY FL.SBJCODE;